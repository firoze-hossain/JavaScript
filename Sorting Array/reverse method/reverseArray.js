//reverse() method reverse elements in an array
//we can use to sort element in descending order
const fruits = [
  "Lemon",
  "Guava",
  "Pineapple",
  "Apple",
  "Ornage",
  "Banana",
  "Grape",
];
console.log("Fruits list: " + fruits);
fruits.sort();
console.log("After sorting: " + fruits);
fruits.reverse();
console.log("After sorting element: " + fruits);

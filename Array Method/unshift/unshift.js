//unshift() method adds element in array at beginning and unshift older element
const fruits = ["Lemon", "Orange", "Apple", "Grape"];
console.log("Before unshifting = " + fruits);
let unshiftedFruit = fruits.unshift("Mango");
console.log("Unshfited Fruit = " + unshiftedFruit);
console.log("After unshifting: " + fruits);

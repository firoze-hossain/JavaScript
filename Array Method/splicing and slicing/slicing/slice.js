//slice() method slices out a piece array into new array
//this example slice out part of an array starting from array element 1
const fruits = ["Mango", "Apple", "Guava", "Pineapple"];
console.log("Total Fruits: " + fruits);
let sliceFruits = fruits.slice(1);
console.log("Sliced Fruits: " + sliceFruits);

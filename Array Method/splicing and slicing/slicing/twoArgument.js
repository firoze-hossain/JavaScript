//slice() method can take two argument
//slice(x,y) starting from x index and finished before y index
const fruits = ["Mango", "Apple", "Orange", "Guava", "Pineapple", "Lemon"];
console.log("Before slicing: " + fruits);
let slicedFruits = fruits.slice(2, 5);
console.log("Sliced fruits list: " + slicedFruits);
